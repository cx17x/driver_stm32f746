void btn_event_handler(lv_event_t *event) {
    lv_obj_t *btn_target = lv_event_get_target(event);
    lv_obj_t *label_obj = lv_obj_get_child(btn_target, 0);

    if (label_obj != NULL) {
        const char *label = lv_label_get_text(label_obj);

		if (strcmp(label, "100 Hz") == 0) {
			state_machine.change_state(BUTTON1_ACTION);
		}
		else if (strcmp(label, "200 Hz") == 0) {
			state_machine.change_state(BUTTON2_ACTION);
		}
		else if (strcmp(label, "duty 30%") == 0) {
			state_machine.change_state(BUTTON3_ACTION);
		}
		else if (strcmp(label, "duty 70%") == 0) {
			state_machine.change_state(BUTTON4_ACTION);
		}
    }
}

